                <p>The glories of Phalguna-Shukla Ekadashi, or Amalaki Ekadashi, are found in the Brahmanda Purana in a
                    conversation between sage Vasishtha and the great King Mandhata.</p>

                <h4>King Mandhata’s Query</h4>
                <p>King Mandhata asked Vasishtha Muni, “O great sage, kindly be merciful and tell me of a holy fast that
                    will bestow me eternal benefit.”</p>

                <p>Vasishtha Muni replied, “O king, listen as I describe the best of all days of fasting, Amalaki
                    Ekadasi. One who faithfully observes a fast on this Ekadashi obtains wealth, is freed from the
                    effects of all kinds of sins, and attains liberation. Fasting on this Ekadasi is even more purifying
                    than donating one thousand cows in charity to a pure brahmana. So please hear me attentively as I
                    tell you the story of a cruel hunter who, though killing innocent animals daily for his living,
                    achieved liberation by inadvertently observing the fast of Amalaki Ekadashi.”</p>
