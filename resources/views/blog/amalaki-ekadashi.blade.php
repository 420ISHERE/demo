    <h4>The Kingdom of Vaidisha</h4>
    <p>“There was once a kingdom named Vaidisha, where all the brahmanas, kshatriyas, vaishyas, and Shudras were equally
        endowed with Vedic knowledge, bodily strength, and fine intelligence. O lion among kings, the whole kingdom
        reverberated with Vedic chants, there was no atheist or sinner. The ruler of this kingdom was King Pashabinduka
        of the lunar Soma dynasty. Also known as Chitraratha he was truthful and religious. He was known to possess the
        strength of ten thousand elephants. He was wealthy and learned in the six branches of the Vedas.”</p>

    <p>“In his kingdom everyone engaged in executing their own dharmas strictly. They were ideal brahmanas, kshatriyas,
        vaishyas, or Sudras. Misers and paupers did not exist in that land. There were no droughts or floods. The
        citizens were healthy and the kingdom was disease free. Following their king, they all engaged themselves in
        devotional service to the Supreme Personality of Godhead, Lord Vishnu. The king also offered special service to
        Lord Shiva. In the kingdom, everyone fasted on the two Ekadasis of every month.</p>

    <p>“In this way, O best of kings, the citizens of Vaidisha lived long years in happiness and prosperity. Avoiding
        materialistic religion, they completely dedicated themselves to the loving service of the Supreme Lord, Hari.”
    </p>

    <h4>King Chitraratha Observes Amalaki Ekadashi</h4>
    <p>“Once, in the month of Phalguna (February – March), the holy fast of Amalaki Ekadashi arrived, conjoined with
        Dvadasi. Realizing this particular Ekadasi to be especially auspicious, King Chitraratha and the citizens of
        Vaidisha carefully observed the fast, meticulously following the rules and regulations.”</p>

    <p>“After bathing in the river, the king and his subjects went to the temple of Lord Vishnu, where an Amalaki tree
        grew. First, the king and his leading priests offered the tree a pot filled with water, a fine canopy, shoes,
        gold, diamonds, rubies, pearls, sapphires, and aromatic incense. Then they worshiped Lord Parashurama with these
        prayers: ‘O Lord Parashurama, O son of Renuka, O all-pleasing one, O liberator of the worlds, please come
        beneath this holy Amalaki tree and accept our humble obeisances.’ Then they prayed to the Amalaki tree: ‘O
        Amalaki, O offspring of Lord Brahma, you can destroy all kinds of sinful reactions. Please accept our respectful
        obeisances and these humble gifts. O Amalaki, you are the form of Brahman and were once worshiped by Lord
        Ramachandra Himself. Whoever circumambulates you is immediately freed from all sins.’</p>

    <p>“After offering these excellent prayers, King Chitraratha and his subjects stayed awake throughout the night,
        praying and worshiping in accordance with the regulations of a sacred Ekadashi fast. At this auspicious time of
        fasting and prayer, an irreligious man approached the assembly. He maintained himself and his family by killing
        animals. Saddled with fatigue as well as sin, the hunter saw the king and the citizens of Vaidisha perform their
        all-night vigil, fasting, and worshiping Lord Vishnu in the beautiful forest setting, brilliantly illuminated
        with lamps. He hid nearby, marveling the extraordinary sight before him.</p>

    <h4>The Fortunate Hunter</h4>
    <p>“‘What is going on here?’ he thought. He saw in that lovely forest beneath the holy Amalaki tree was a Deity of
        Lord Damodara being worshiped on a water pot asana and heard devotees singing sacred hymns extolling Lord
        Krishna’s transcendental forms and pastimes. Despite himself, that unholy killer of innocent birds and animals
        spent the entire night in amazement watching the Ekadasi rituals and hearing the glorification of the Lord.</p>

    <p>“At dawn, the king with his royal retinue – including the court priests and citizens – concluded their observance
        of Ekadashi and returned to the city of Vaidisha. The hunter returned to his hut and ate his meal. In due
        course, the hunter died. However, by the merit he had gained by fasting on Amalaki Ekadashi and hearing the
        glorification of the Supreme Personality of Godhead, as well as remaining awake all night, he became eligible to
        be reborn as a great king endowed with chariots, elephants, horses, and soldiers. His name was Vasuratha, the
        son of King Viduratha, and he ruled over the kingdom of Jayanti.</p>

    <h4>King Vasuratha</h4>
    <p>“King Vasuratha, strong and fearless, was effulgent as the Sun, and handsome as the Moon. In strength, he was
        like Sri Vishnu, and in forgiveness like the Earth. Charitable and truthful, King Vasuratha constantly offered
        loving devotional service to the Supreme Lord, Sri Vishnu. He was well versed in Vedic knowledge. Dynamic in
        affairs of the state, he enjoyed caring for his subjects as his own children. He disliked pride and would crush
        it in anyone he saw proud. He performed many sacrifices and ensured that the needy received charity.”</p>

    <p>“Once, while hunting in the jungle, King Vasuratha lost his way. Wandering around he eventually grew weary. Using
        his arms as a pillow, he fell asleep beneath a tree. Meanwhile, some barbarian tribesmen arrived there. Long
        inimical to the king, they began discussing ways of killing him. One of them said, ‘He has killed our fathers,
        mothers, brothers-in-law, grandsons, nephews, and uncles, and so we are forced to aimlessly wander like madmen
        in the forest.’</p>

    <h4>The King Saved</h4>
    <p>They then attempted to kill King Vasuratha with various deadly weapons, including spears, swords, arrows, and
        mystic ropes. However, none of these weapons could even touch the sleeping king. Out of sheer fright, the
        uncivilized, dog-eating tribesmen were sapped of all strength. Losing whatever little intelligence they had,
        they were rendered almost unconscious with bewilderment and weakness.</p>

    <p>“Suddenly a beautiful woman effulgent with ornaments, emitting a wonderful fragrance, with an excellent garland
        around her neck, eyebrows drawn in fierce anger, and eyes fiery red, leaped out of the king’s body. She looked
        like death personified. With her blazing chakra discus, she quickly killed all the tribal hunters.”</p>

    <p>“The king awoke and was astonished to see dead tribesmen lying all around him. He wondered, ‘Who has slain these
        great enemies of mine so violently? Who is my benefactor?’ At that moment a voice from the sky spoke, ‘You
        wanted to know who helped you! Who is that person who alone can help one in distress? It is none other than Sri
        Keshava, the Supreme Personality of Godhead. He is the one who protects all who take complete shelter of Him
        without a tinge of selfish motive.’ King Vasuratha was overwhelmed with love for the Personality of Godhead Sri
        Keshava (Krishna).”</p>

    <p>“He returned to his capital and ruled like Lord Indra without any obstacles.”</p>

    <h4>Conclusion</h4>
    <p>“Therefore, O King Mandhata,” the venerable Vasishtha Muni concluded, “anyone who observes this holy Amalaki
        Ekadasi will undoubtedly attain the supreme abode of Lord Vishnu. Such is the unfathomable religious merit
        earned by observing this sacred day of fasting.”</p>
