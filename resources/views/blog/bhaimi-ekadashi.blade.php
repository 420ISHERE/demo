        <p data-aos="fade-up" data-aos-delay="200">The narration of the glories of Magha-Shukla Ekadasi or Jaya Ekadashi
            is found in the <em>Bhavishyottara Purana</em> in a conversation between Maharaja Yudhishthira and Lord
            Krishna. It is said that one who observes <em>upavasa</em> (fasting) on this day is granted entry into Lord
            Vishnu’s abode, even not having performed other <em>vratas</em> of the year. The half-day fast
            (<em>vrata</em>) for the appearance day of Lord Varahadev on the following day, Varaha Dvadasi, is also
            observed on this Ekadashi.</p>

        <h3 class="mt-5 mb-3 fw-bold" data-aos="fade-up">Yudhishthira Maharaja Enquires From Lord Krishna</h3>
        <p data-aos="fade-up" data-aos-delay="100">Yudhisthira Maharaj said, “O Lord of the lords, Sri Krishna, all
            glories unto You! O Master of the universe, You alone are the source of the four types of living entities –
            those born of eggs, of perspiration, of seeds, and of embryos. You are the root cause of all, O Lord, and
            therefore the creator, maintainer, and destroyer.</p>

        <p data-aos="fade-up" data-aos-delay="200">“My Lord, You have kindly described to me the auspicious day known as
            Sat-tila Ekadashi, which occurs during the dark fortnight of the month of Magha (January – February). Now I
            request you to please explain to me about the Ekadasi that occurs in the bright fortnight of this month. By
            what name is it known, and what is the process for observing it? Who is the presiding Deity of this sublime
            day, which is so dear to You?”</p>

        <h3 class="mt-5 mb-3 fw-bold" data-aos="fade-up">Lord Krishna Answers Yudhishthira</h3>
        <p data-aos="fade-up" data-aos-delay="100">Lord Sri Krishna replied, “O Yudhisthira, I shall be glad to tell you
            about the Ekadasi that occurs during the bright half of Magha. This Ekadashi effaces all kinds of sinful
            reactions and demonic influences. It is known as Jaya Ekadashi, and the fortunate soul who fasts on this
            sacred day is relieved of the great burden of ghostly existence. Thus there is no better Ekadashi than this,
            for it truly bestows freedom from birth and death. It should be observed respectfully and meticulously.
            Please listen to Me attentively, O Pandava, as I relate an ancient episode that I have previously narrated
            in the Padma Purana.</p>

        <h3 class="mt-5 mb-3 fw-bold" data-aos="fade-up">Malyavan and Pushpavati Attracted</h3>
        <p data-aos="fade-up" data-aos-delay="100">Long ago, Lord Indra ruled his celestial kingdom well and the devas
            living there were content. In the Nandana Forest graced with Parijata Flowers, Indra drank ambrosia and
            enjoyed the company of celestial maidens (Apsaras), who danced for his pleasure. Chitrasena, Indra’s chief
            musician, his wife Malini, and his handsome son Malyavan entertained Indra.</p>

        <p data-aos="fade-up" data-aos-delay="200">At that time, an Apsara named Pushpavati became attracted to
            Malyavan. Cupid’s sharp arrows pierced her heart. With her beautiful body, complexion and enchanting
            movements of eyebrows, she captivated Malyavan.</p>

        <h3 class="mt-5 mb-3 fw-bold" data-aos="fade-up">Indra Curses Malyavan and Pushpavati</h3>
        <p data-aos="fade-up" data-aos-delay="100">Seeing Pushpavati in all her heavenly beauty, Malyavan was bewitched.
            They faltered in their singing and dancing before Indra, who became angry and cursed them both to suffer as
            <em>pisachas</em> (hobgoblins) on Earth.</p>

        <h3 class="mt-5 mb-3 fw-bold" data-aos="fade-up">The Sufferings of Malyavan and Pushpavati</h3>
        <p data-aos="fade-up" data-aos-delay="100">Dumbstruck by the chastisement, they fell from the heavenly Nandana
            Forest to the Himalayas. There they suffered severely in the freezing cold, their celestial intelligence
            diminished. They wandered in agony, lamenting their misdeeds.</p>

        <h3 class="mt-5 mb-3 fw-bold" data-aos="fade-up">The Blessings of Bhaimi Ekadashi</h3>
        <p data-aos="fade-up" data-aos-delay="100">By good fortune, that day was Jaya (Bhaimi) Ekadashi. In their
            misery, they did not eat or drink anything, unknowingly observing a complete fast and staying awake all
            night. The next morning, Dvadasi dawned, and by the mercy of Lord Vishnu, they were released from their
            curse and regained their celestial forms. A divine airplane arrived and took them back to Indra’s abode.</p>

        <h3 class="mt-5 mb-3 fw-bold" data-aos="fade-up">Lord Krishna Concludes</h3>
        <p data-aos="fade-up" data-aos-delay="100">Lord Krishna said, “O Yudhishthira, one should strictly fast on the
            sacred day of Lord Hari, especially on Jaya Ekadashi, which frees one even from the sin of killing a
            twice-born Brahmin. Fasting on this Ekadashi qualifies one to reside in Vaikuntha and enjoy eternal
            happiness.”</p>

        <p data-aos="fade-up" data-aos-delay="200">“O great king,” Lord Sri Krishna concluded, “one who even hears or
            reads these wonderful glories of Jaya Ekadashi attains the merit of performing an Agnistoma fire sacrifice,
            during which the hymns from the Sama-Veda are recited.”</p>

